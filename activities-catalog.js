const SEA_BRING = [
  "Вода",
  "Полотенце",
  "Солнцезащита",
  "Легкая кофта на вечер или ветер"
];

const LAND_BRING = [
  "Вода",
  "Удобная закрытая обувь",
  "Головной убор",
  "Телефон или камера"
];

const ACTIVE_BRING = [
  "Вода",
  "Закрытая спортивная обувь",
  "Удобная одежда по погоде",
  "Телефон или экшен-камера"
];

function buildFaq(activity) {
  const isSea = (activity.categoryIds || []).includes("sea");
  if (isSea) {
    return [
      {
        question: "Можно ли с детьми?",
        answer: activity.childrenAdvice
      },
      {
        question: "Что взять с собой?",
        answer: (activity.whatToBring || []).join(", ") + "."
      },
      {
        question: "Сколько длится прогулка?",
        answer: `Ориентировочная длительность: ${activity.duration}.`
      },
      {
        question: "Зависит ли выход от погоды?",
        answer: activity.weatherAdvice
      }
    ];
  }

  return [
    {
      question: "Подходит ли маршрут новичкам?",
      answer: "Да, темп и сложность уточняются заранее. Можно выбрать спокойный или более активный формат."
    },
    {
      question: "Что взять с собой?",
      answer: (activity.whatToBring || []).join(", ") + "."
    },
    {
      question: "Сколько длится поездка?",
      answer: `Ориентировочная длительность: ${activity.duration}.`
    },
    {
      question: "Что делать, если погода меняется?",
      answer: activity.weatherAdvice
    }
  ];
}

function defaultBestFor(activity) {
  const ids = activity.categoryIds || [];
  if (ids.includes("sea") && ids.includes("kids")) return ["Семьи с детьми", "Пары", "Спокойный отдых", "Фото у моря"];
  if (ids.includes("sea")) return ["Пары", "Друзья", "Летний отдых", "Морские впечатления"];
  if (ids.includes("waterfalls")) return ["Любители природы", "Семьи", "Фото-маршруты", "Однодневные поездки"];
  if (ids.includes("quads")) return ["Активный отдых", "Друзья", "Экстрим", "Горные маршруты"];
  if (ids.includes("horses")) return ["Спокойный отдых", "Пары", "Семьи", "Прогулки на природе"];
  if (ids.includes("sup")) return ["Пары", "Семьи", "Активные туристы", "Летний отдых"];
  if (ids.includes("diving")) return ["Любители моря", "Пары", "Друзья", "Новые впечатления"];
  return ["Туристы", "Пары", "Семьи", "Друзья"];
}

function defaultWhatToBring(activity) {
  const ids = activity.categoryIds || [];
  if (ids.includes("quads")) return ACTIVE_BRING;
  if (ids.includes("sea") || ids.includes("diving") || ids.includes("sup")) return SEA_BRING;
  return LAND_BRING;
}

function defaultWeatherAdvice(activity) {
  const ids = activity.categoryIds || [];
  if (ids.includes("sea") || ids.includes("diving") || ids.includes("sup")) {
    return "Маршрут, купание, остановки, выход на берег и дополнительные активности зависят от погоды, волны, обстановки на воде и решения капитана. Капитан принимает финальное решение по безопасности.";
  }
  if (ids.includes("quads")) {
    return "После дождя покрытие может быть скользким. Уточняйте условия маршрута перед стартом.";
  }
  return "Погодные условия и комфорт маршрута лучше уточнить в день поездки.";
}

function defaultChildrenAdvice(activity) {
  const ids = activity.categoryIds || [];
  if (ids.includes("kids")) {
    return "Формат подходит для семейного отдыха. Для маленьких детей лучше выбирать спокойное время и короткую длительность.";
  }
  if (ids.includes("sea")) {
    return "Возможность участия детей зависит от погоды и выбранного формата, лучше уточнить заранее.";
  }
  return "С детьми можно, если выбрать комфортный темп и учитывать возраст ребенка.";
}

function defaultLimits(activity) {
  const ids = activity.categoryIds || [];
  if (ids.includes("sea") || ids.includes("diving") || ids.includes("sup")) {
    return "Выход зависит от погодных условий и рекомендаций экипажа.";
  }
  if (ids.includes("quads")) {
    return "Маршрут зависит от состояния трассы и погодных условий.";
  }
  return "Условия и ограничения уточняются перед выездом.";
}

function defaultAges(activity) {
  return (activity.categoryIds || []).includes("kids")
    ? "С детьми по согласованию"
    : "Возрастные условия уточняются перед бронированием";
}

function normalizeActivity(activity) {
  const bestFor = activity.bestFor || defaultBestFor(activity);
  const whatToBring = activity.whatToBring || defaultWhatToBring(activity);
  const weatherAdvice = activity.weatherAdvice || defaultWeatherAdvice(activity);
  const childrenAdvice = activity.childrenAdvice || defaultChildrenAdvice(activity);

  return {
    ...activity,
    shortDescription: activity.shortDescription || activity.short,
    bestFor,
    whatToBring,
    weatherAdvice,
    childrenAdvice,
    audience: activity.audience || bestFor.join(", "),
    bring: activity.bring || whatToBring.join(", "),
    limits: activity.limits || defaultLimits(activity),
    ages: activity.ages || defaultAges(activity),
    faq: activity.faq || buildFaq({ ...activity, bestFor, whatToBring, weatherAdvice, childrenAdvice })
  };
}

