if (refreshIntervalMs > 0 && typeof setInterval === "function") {
  const timer = setInterval(() => {
    refreshActivitiesCatalog().catch(() => {});
  }, refreshIntervalMs);
  if (typeof timer?.unref === "function") timer.unref();
}

